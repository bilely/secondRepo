package business;

public class Poisson extends Proteine {
	
	String origine;

	
	public Poisson() {
		super();
	}
	
	public Poisson(String nom) {
		super();
	}

	public Poisson(String nom, String origine) {
		super();
		this.origine = origine;
	}

	public String getOrigine() {
		return origine;
	}

	public void setOrigine(String origine) {
		this.origine = origine;
	}

	@Override
	public String toString() {
		return "Poisson [" + (origine != null ? "origine=" + origine : "") + "]";
	}
	
	

}
