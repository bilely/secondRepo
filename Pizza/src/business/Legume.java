package business;

public class Legume {
	private String nom;
	private String type ;
	
	
	public Legume() {
		super();
	
	}
	
	public Legume(String nom) {
		super();
		this.nom = nom;
	}
	
	public Legume(String nom, String type) {
		super();
		this.nom = nom;
		this.type = type;
	}
	
	public String getNom() {
		return nom;
	}
	public void setNom(String nom) {
		this.nom = nom;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	@Override
	public String toString() {
		return "Legume [" + (nom != null ? "nom=" + nom + ", " : "") + (type != null ? "type=" + type : "") + "]";
	}
	
	
	
	
	

}
